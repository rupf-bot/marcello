const rateLimitMap = new Map();

function checkRateLimit(ip) {
  const now = Date.now();
  const windowMs = 60 * 1000;
  const max = 25;
  const entry = rateLimitMap.get(ip) || { count: 0, reset: now + windowMs };
  if (now > entry.reset) {
    entry.count = 0;
    entry.reset = now + windowMs;
  }
  entry.count++;
  rateLimitMap.set(ip, entry);
  return entry.count <= max;
}


async function askGroqWithRetry(history, lang = 'de') {
  const apiKey = process.env.GROQ_API_KEY;
  if (!apiKey) throw new Error("GROQ_API_KEY not configured");

  const langInstruction = lang === "en"
    ? "IMPORTANT: The page is set to English. You MUST respond in English only."
    : "IMPORTANT: Die Seite ist auf Deutsch. Antworte auf Deutsch.";

  const messages = [
    { role: "system", content: SYSTEM_PROMPT + "\n\n" + langInstruction },
    ...history.map(m => ({
      role: m.role === "model" ? "assistant" : "user",
      content: m.parts[0].text
    }))
  ];

  for (let i = 0; i < 3; i++) {
    const controller = new AbortController();
    const timeout = setTimeout(() => controller.abort(), 15000);

    let response;
    try {
      response = await fetch("https://api.groq.com/openai/v1/chat/completions", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          "Authorization": `Bearer ${apiKey}`
        },
        body: JSON.stringify({
          model: "llama-3.3-70b-versatile",
          messages,
          temperature: 0.7,
          max_tokens: 400
        }),
        signal: controller.signal
      });
    } finally {
      clearTimeout(timeout);
    }

    if (response.status === 429) {
      await new Promise(r => setTimeout(r, 3000 * (i + 1)));
      continue;
    }

    if (!response.ok) {
      const err = await response.json();
      throw new Error(err.error?.message || "Groq error");
    }

    const data = await response.json();
    return data.choices?.[0]?.message?.content || null;
  }

  throw new Error("Quota exceeded");
}

export default async function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "https://marcelrupf.com");
  res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") return res.status(200).end();
  if (req.method !== "POST") return res.status(405).json({ reply: "Method not allowed" });

  const ip = req.headers["x-forwarded-for"] || "unknown";
  if (!checkRateLimit(ip)) {
    return res.status(429).json({ reply: "Bitte warte kurz, bevor du die nächste Frage stellst." });
  }

  const { history, lang } = req.body;
  if (!history || !Array.isArray(history)) {
    return res.status(400).json({ reply: "Ungültige Anfrage." });
  }

  const isEn = lang === 'en';
  try {
    const reply = await askGroqWithRetry(history, lang || 'de');
    if (!reply) return res.json({ reply: isEn ? "No answer received. Please try again." : "Keine Antwort erhalten. Bitte versuch es nochmal." });
    res.json({ reply });
  } catch (err) {
    console.error("Chat error:", err.message);
    if (err.name === "AbortError") {
      return res.json({ reply: isEn ? "Request timed out. Please try again." : "Anfrage hat zu lange gedauert. Bitte versuch es nochmal." });
    }
    if (err.message === "GROQ_API_KEY not configured") {
      return res.status(500).json({ reply: isEn ? "Service not configured." : "Service nicht konfiguriert." });
    }
    res.json({ reply: isEn ? "I'm briefly unavailable. Please try again in a moment." : "Ich bin gerade kurz beschäftigt. Bitte versuch es in einem Moment nochmal." });
  }
}
